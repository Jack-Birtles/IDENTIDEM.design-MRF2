const size_t NUM_LENSES = sizeof(lenses) / sizeof(lenses[0]);

int getLensDistancePointCount(const Lens &lens)
{
  int point_count = 0;
  float previous_distance = 0.0f;

  for (int i = 0; i < LENS_DISTANCE_POINT_COUNT; i++)
  {
    float distance_marker = lens.distance[i];
    if (distance_marker <= 0.0f)
    {
      break;
    }

    if (point_count > 0 && distance_marker <= previous_distance)
    {
      break;
    }

    previous_distance = distance_marker;
    point_count++;
  }

  return point_count;
}
